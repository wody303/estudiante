package com.mycompany;

public class estudiante {

    private String materia, nombreE, primerP, segundoP, examenF;

    public estudiante() {
    }

    public String getMateria() {
        return materia;
    }

    public void setMateria(String materia) {
        this.materia = materia;
    }

    public String getNombreE() {
        return nombreE;
    }

    public void setNombreE(String nombreE) {
        this.nombreE = nombreE;
    }

    public String getPrimerP() {
        return primerP;
    }

    public void setPrimerP(String primerP) {
        this.primerP = primerP;
    }

    public String getSegundoP() {
        return segundoP;
    }

    public void setSegundoP(String segundoP) {
        this.segundoP = segundoP;
    }

    public String getExamenF() {
        return examenF;
    }

    public void setExamenF(String examenF) {
        this.examenF = examenF;
    }
}
